 * Для заданного числа n определить, содержит ли оно различающиеся цифры.
 * Например, 54 и 323 состоят из разных цифр, а 111 и 0 из одинаковых.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun hasDifferentDigits(n: Int): Boolean {
    var number = n
    var symbol1: Int
    var symbol2: Int

    if (number in 0..9) return false

    while (number > 99) {
        symbol1 = number % 10
        number /= 10
        symbol2 = number % 10
        number /= 10
        if (symbol1 != symbol2) return true
        if (number in 0..9) {
            symbol1 = number % 10
            if (symbol1 != symbol2) return true
        }
    }
    if (number > 9) {
        symbol1 = n % 10
        symbol2 = n / 10
        if (symbol1 != symbol2) return true
    }
    return false
}

/**
 * Сложная
 *
 * Найти n-ю цифру последовательности из квадратов целых чисел:
 * 149162536496481100121144...
 * Например, 2-я цифра равна 4, 7-я 5, 12-я 6.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun squareSequenceDigit(n: Int): Int {
    var number = 0
    var sqrOfNumber = 0
    var numeralInLine = 0
    var counter: Int
    while (numeralInLine < n) {
        number++
        sqrOfNumber = sqr(number)
        counter = digitNumber(sqrOfNumber)
        numeralInLine += counter
    }
    if (numeralInLine - n == 0) return sqrOfNumber % 10
    return (sqrOfNumber / (10.0.pow(abs(n - numeralInLine))).toInt()) % 10
}

/**
 * Сложная
 *
 * Найти n-ю цифру последовательности из чисел Фибоначчи (см. функцию fib выше):
 * 1123581321345589144...
 * Например, 2-я цифра равна 1, 9-я 2, 14-я 5.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun fibSequenceDigit(n: Int): Int {
    var fib1 = 1
    var fib2 = 1
    var fibN = 0
    var counter: Int
    var numeralInLine = 3

    if (n == 1 || n == 2) return 1

    while (numeralInLine <= n) {
        fibN = fib1 + fib2
        fib1 = fib2
        fib2 = fibN
        counter = digitNumber(fibN)
        numeralInLine += counter
    }
    numeralInLine--
    if (numeralInLine - n == 0) return fibN % 10
    return (fibN / (10.0.pow(abs(n - numeralInLine))).toInt()) % 10
}
